import fs from "fs"
import path from "path"

export type AnalysisDatasetKey =
  | "outlets"
  | "predictions"
  | "budget"
  | "coordinates"

export type AnalysisDataset = {
  key: AnalysisDatasetKey
  label: string
  filename: string
  description: string
}

export const ANALYSIS_DATASETS: AnalysisDataset[] = [
  {
    key: "outlets",
    label: "Western Province outlets",
    filename: "outlets.json",
    description: "Merged outlet, prediction, coordinate, and spend view.",
  },
  {
    key: "predictions",
    label: "Latent demand predictions",
    filename: "predictions.json",
    description: "Maximum monthly liters produced by the analysis pipeline.",
  },
  {
    key: "budget",
    label: "Budget allocations",
    filename: "budget_allocations.json",
    description: "Trade spend allocations generated by the optimizer.",
  },
  {
    key: "coordinates",
    label: "Outlet coordinates",
    filename: "outlet_coordinates.json",
    description: "Raw outlet latitude and longitude exported for web use.",
  },
]

export const DATA_DIR = path.join(process.cwd(), "public", "data")

export function getDatasetConfig(key: string) {
  return ANALYSIS_DATASETS.find((dataset) => dataset.key === key)
}

export function getDatasetPath(dataset: AnalysisDataset) {
  return path.join(DATA_DIR, dataset.filename)
}

export function readDataset(dataset: AnalysisDataset) {
  const datasetPath = getDatasetPath(dataset)

  if (!fs.existsSync(datasetPath)) {
    return []
  }

  return JSON.parse(fs.readFileSync(datasetPath, "utf-8")) as unknown[]
}

export function getDatasetSummary(dataset: AnalysisDataset) {
  const datasetPath = getDatasetPath(dataset)
  const exists = fs.existsSync(datasetPath)
  const stats = exists ? fs.statSync(datasetPath) : null
  const rows = exists ? readDataset(dataset).length : 0

  return {
    key: dataset.key,
    label: dataset.label,
    description: dataset.description,
    filename: dataset.filename,
    rows,
    bytes: stats?.size ?? 0,
    updatedAt: stats?.mtime.toISOString() ?? null,
    streamUrl: `/api/backend/stream?dataset=${dataset.key}`,
  }
}
